"""Basic math operations."""

def add(a, b):
    c = int(a)+ int(b)
    
    return c

def sub(a, b):
    """Substract b from a."""
    c = int(a)- int(b)
    return c

def mult(a, b):
    """Multiply a and b."""
    c = int(a) * int(b)
    return c

def div(a, b):
    """Divide a by b."""
    c= int(a)/int(b)
    return c
